function check_skills()
	local l_skills = " "
	if matches[4] then
		l_skills = l_skills .. matches[4]
	end
	if matches[5] then
		l_skills = l_skills .. matches[5]
	end
	if matches[6] then
		l_skills = l_skills .. matches[6]
	end
	l_skills = utf8.trim(l_skills)
	skills[l_skills] = skills[l_skills] or {}
	skills[l_skills].lvl = tonumber(matches[7])
	skills[l_skills].pot = tonumber(matches[8])
	skills[l_skills].name = utf8.trim(matches[3])
	if skills[l_skills].lvl >= 300 then
		skills[l_skills].mstlvl = 300
	end
	if l_skill == "force" and skills[l_skills].mstlvl < 220 and skills[l_skills].mstlvl == hp.pot_max - 101 then
		skills[l_skills].mstlvl = nil
	end
	-- GUI 刷新显示技能
	GUIShow_Skills()
end

function jifaAll()
    for p in pairs(skills) do
        local sk = qrySkillEnable(p)
        if sk and sk["force"] and perform.force and perform.force == p then
            exe("jifa force " .. p)
        end
        if sk and not sk["force"] then
            for q in pairs(sk) do
                if skills[q] and skills[p].lvl >= skills[q].lvl then
                    exe("jifa " .. q .. " " .. p)
                end
            end
        end
    end
end

function qrySkillEnable(sk)
	local ske = skillsEnable[sk]
	-- ain debug
	if not ske then
		return false
	end
	local skl = {}
	if type(ske) == "function" then
		if ske() then
			skl = string.split(ske(), ",")
		else
			return false
		end
	else
		skl = string.split(ske, ",")
	end
	local skr = {}
	for _, p in pairs(skl) do
		skr[p] = true
	end
	if countTab(skr) > 0 then
		return skr
	else
		return false
	end
end

